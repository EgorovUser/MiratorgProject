using System.Collections.Generic;
using System.Text;

namespace TxtTableParsing
{
    /// <summary>
    /// Настройки парсера табличных файлов.
    /// Все свойства имеют значения по умолчанию, типичные для SAP ALV-выгрузок.
    /// </summary>
    public class ParserOptions
    {
        /// <summary>
        /// Разделитель столбцов.
        /// По умолчанию — табуляция ('\t').
        /// </summary>
        public char Delimiter { get; set; } = '\t';

        /// <summary>
        /// Максимальное количество строк (от начала файла),
        /// в которых ищется строка заголовков.
        /// По умолчанию — 20.
        /// </summary>
        public int MaxHeaderSearchRows { get; set; } = 20;

        /// <summary>
        /// Столбцы, которые обязательно должны присутствовать в строке заголовков.
        /// Если хотя бы один из них не найден — парсер выбрасывает исключение.
        /// По умолчанию — пустой массив (проверка не выполняется).
        /// </summary>
        public string[] RequiredColumns { get; set; } = new string[0];

        /// <summary>
        /// Маркеры итоговых / промежуточных строк.
        /// Если любое поле строки (после Trim) совпадает с одним из маркеров —
        /// строка пропускается.
        /// По умолчанию: "***", "**", "*".
        /// </summary>
        public HashSet<string> SummaryMarkers { get; set; } =
            new HashSet<string> { "***", "**", "*" };

        /// <summary>
        /// Если задан (не null), поле с этим индексом в каждой строке проверяется
        /// на содержание 4-значного года. Строки, не прошедшие проверку, пропускаются.
        /// По умолчанию: null (проверка не выполняется).
        /// </summary>
        public int? YearFieldIndex { get; set; }

        /// <summary>
        /// Минимальный допустимый год при валидации строки данных.
        /// Игнорируется, если <see cref="YearFieldIndex"/> равен null.
        /// По умолчанию — 2000.
        /// </summary>
        public int MinYear { get; set; } = 2000;

        /// <summary>
        /// Максимальный допустимый год при валидации строки данных.
        /// Игнорируется, если <see cref="YearFieldIndex"/> равен null.
        /// По умолчанию — 2100.
        /// </summary>
        public int MaxYear { get; set; } = 2100;

        /// <summary>
        /// Кодировки для попытки чтения файла (по порядку).
        /// Файл пробуется прочитать первой кодировкой; если в результате
        /// обнаруживаются некорректные символы — пробуется следующая.
        /// По умолчанию: UTF-8.
        /// </summary>
        public Encoding[] Encodings { get; set; } =
        {
            Encoding.UTF8,
        };
    }
}
